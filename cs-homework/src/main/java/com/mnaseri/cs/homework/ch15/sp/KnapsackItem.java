package com.mnaseri.cs.homework.ch15.sp;

public class KnapsackItem {

    private int size, value;

    public KnapsackItem(int size, int value) {
        this.size = size;
        this.value = value;
    }

    public int getSize() {
        return size;
    }

    public int getValue() {
        return value;
    }
}
